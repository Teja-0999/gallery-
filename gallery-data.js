// Helper Functions
const GalleryData = {
    // Get all artworks
    getAllArtworks: function() {
        return ARTWORKS;
    },
    
    // Get artwork by ID
    getArtworkById: function(id) {
        return ARTWORKS.find(artwork => artwork.id === id);
    },
    
    // Get artworks by category
    getArtworksByCategory: function(category) {
        return ARTWORKS.filter(artwork => artwork.category === category);
    },
    
    // Get artworks by artist ID
    getArtworksByArtist: function(artistId) {
        return ARTWORKS.filter(artwork => artwork.artistId === artistId);
    },
    
    // Get available artworks
    getAvailableArtworks: function() {
        return ARTWORKS.filter(artwork => artwork.status === 'available');
    },
    
    // Get featured artworks (top 20 by engagement)
    getFeaturedArtworks: function() {
        return [...ARTWORKS]
            .sort((a, b) => {
                const likesA = Number(a.likes) || 0;
                const likesB = Number(b.likes) || 0;
                const viewsA = Number(a.views) || 0;
                const viewsB = Number(b.views) || 0;
                return (likesB + viewsB) - (likesA + viewsA);
            })
            .slice(0, 20);
    },
    
    // Get all artists
    getAllArtists: function() {
        return ARTISTS;
    },
    
    // Get artist by ID
    getArtistById: function(id) {
        return ARTISTS.find(artist => artist.id === id);
    },
    
    // Get featured artists
    getFeaturedArtists: function() {
        return ARTISTS.filter(artist => artist.featured);
    },
    
    // Get all exhibitions
    getAllExhibitions: function() {
        return EXHIBITIONS;
    },
    
    // Get exhibition by ID
    getExhibitionById: function(id) {
        return EXHIBITIONS.find(exhibition => exhibition.id === id);
    },
    
    // Get current exhibitions
    getCurrentExhibitions: function() {
        return EXHIBITIONS.filter(exhibition => exhibition.status === 'current');
    },
    
    // Get upcoming exhibitions
    getUpcomingExhibitions: function() {
        return EXHIBITIONS.filter(exhibition => exhibition.status === 'upcoming');
    },
    
    // Search artworks
    searchArtworks: function(query) {
        const searchTerm = query.toLowerCase();
        return ARTWORKS.filter(artwork => 
            artwork.title.toLowerCase().includes(searchTerm) ||
            artwork.artist.toLowerCase().includes(searchTerm) ||
            artwork.category.toLowerCase().includes(searchTerm) ||
            artwork.description.toLowerCase().includes(searchTerm)
        );
    },
    
    // Sort artworks
    sortArtworks: function(artworks, sortBy) {
        const sorted = [...artworks];
        switch(sortBy) {
            case 'newest':
                return sorted.sort((a, b) => new Date(b.date) - new Date(a.date));
            case 'oldest':
                return sorted.sort((a, b) => new Date(a.date) - new Date(b.date));
            case 'title':
                return sorted.sort((a, b) => a.title.localeCompare(b.title));
            case 'price-low':
                return sorted.sort((a, b) => a.price - b.price);
            case 'price-high':
                return sorted.sort((a, b) => b.price - a.price);
            case 'popular':
                return sorted.sort((a, b) => b.likes - a.likes);
            default:
                return sorted;
        }
    }
};

